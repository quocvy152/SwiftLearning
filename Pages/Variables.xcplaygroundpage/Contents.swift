import Foundation

// Declare with let keyword that like constant (cannot assign another value)
let firstItem: Bool = false

// Declare with var can assign variable for another value
var secondeItem: Bool = false
secondeItem = true

print(firstItem, secondeItem)

// if statements
var heIsSoccerPlayer = true
if heIsSoccerPlayer == true {
    print("He is soccer player!")
}

if heIsSoccerPlayer {
    print("He is soccer player, second time!")
}

  
